public class Sum {

	/**
	 * 
	 */
	private final long A;
	private final long B;
	
	public Sum(long a, long b) {
		
		this.A = a;
		this.B = b;
	}

	public long getA() {
		return A;
	}

	public long getB() {
		return B;
	}
	
	
}
